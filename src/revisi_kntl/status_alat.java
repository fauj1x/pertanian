    private void jLabel1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel1MouseClicked
        String searchInput = jTextField1.getText().trim();

    // Check if the search input is not empty
    if (!searchInput.isEmpty()) {
        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
        TableRowSorter<DefaultTableModel> rowSorter = new TableRowSorter<>(tableModel);
        jTable1.setRowSorter(rowSorter);

        // Specify the column index you want to search in (adjust as needed)
        int columnIndexToSearch = 1; // Assuming you want to search in the second column (index 1)

        // Set the RowFilter to perform case-insensitive search
        rowSorter.setRowFilter(RowFilter.regexFilter("(?i)" + searchInput, columnIndexToSearch));
    } else {
        // If search input is empty, reset the row sorter to show all rows
        jTable1.setRowSorter(null);
    }
    }//GEN-LAST:event_jLabel1MouseClicked

