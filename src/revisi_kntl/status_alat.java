    private void saveDataToDatabase(String namaAlat, int jumlahAlat, double hargaSewa) {
    try (Connection connection = koneksi.GetConnection();
         PreparedStatement preparedStatement = connection.prepareStatement("INSERT INTO alat (Nama_Alat, jumlah_Alat, Harga_sewa) VALUES (?, ?, ?)")) {

        preparedStatement.setString(1, namaAlat);
        preparedStatement.setInt(2, jumlahAlat);
        preparedStatement.setDouble(3, hargaSewa);

        int result = preparedStatement.executeUpdate();

        if (result > 0) {
            JOptionPane.showMessageDialog(rootPane, "Data berhasil disimpan ke dalam database.");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Gagal menyimpan data ke dalam database.");
        }

    } catch (SQLException | HeadlessException e) {
        JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
    }
}
    
private void loadDataToTable() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("id_Hpinjam");
    model.addColumn("Tgl_pinjam");
    model.addColumn("Tenggat_peminjaman");
    model.addColumn("Username");
    model.addColumn("id_Detpinjam");
    model.addColumn("status_peminjaman");
    model.addColumn("id_Detpinjam");
    model.addColumn("Total_alat");
    model.addColumn("jumlah_Alatpinjam");
    model.addColumn("id_alat");

    try (Connection connection = koneksi.GetConnection();
         Statement statement = connection.createStatement();
         ResultSet resultSet = statement.executeQuery("select * from meminjam join detail_peminjaman")) {

        while (resultSet.next()) {
            String id_Hpinjam = resultSet.getString("id_Hpinjam");
            String Tgl_pinjam = resultSet.getString("Tgl_pinjam");
            String Tenggat_peminjaman = resultSet.getString("Tenggat_peminjaman");
            String Username = resultSet.getString("Username");
            String id_Detpinjam = resultSet.getString("id_Detpinjam");
            String status_peminjaman = resultSet.getString("status_peminjaman");
            String Total_alat = resultSet.getString("Total_alat");
            String jumlah_Alatpinjam = resultSet.getString("jumlah_Alatpinjam");
            String id_alat = resultSet.getString("id_alat");

            model.addRow(new Object[]{id_Hpinjam, Tgl_pinjam, Tenggat_peminjaman, Username, id_Detpinjam, status_peminjaman, id_Detpinjam, Total_alat, jumlah_Alatpinjam, id_alat});
        }

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Error loading data from database: " + e.getMessage());
    }

    jTable1.setModel(model);
}
