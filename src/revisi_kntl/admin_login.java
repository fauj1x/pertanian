 public class AdminAuthenticator {

        public boolean authenticate(String username, char[] password) {
            String passwordStr = new String(password);

            try (Connection connection = koneksi.GetConnection();
                PreparedStatement preparedStatement = connection.prepareStatement("SELECT * FROM Admin WHERE Username = ? AND Password = ?")) {

                preparedStatement.setString(1, username);
                preparedStatement.setString(2, passwordStr);

                try (ResultSet resultSet = preparedStatement.executeQuery()) {
                    return resultSet.next(); // true if the result set is not empty
                }

            } catch (SQLException e) {
                e.printStackTrace(); // Handle or log the exception appropriately
                return false;
            }
        }
    }
