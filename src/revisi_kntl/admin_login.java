    private void jLabel2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel2MouseClicked
 String username = jTextField1.getText();
    char[] passwordChars = jPasswordField1.getPassword();

    if (username.isEmpty() || passwordChars.length == 0) {
        JOptionPane.showMessageDialog(rootPane, "Isi nama pengguna dan kata sandi sebelum login.");
        return;
    }

    // Buat objek AdminAuthenticator
    AdminAuthenticator adminAuthenticator = new AdminAuthenticator();

    // Lakukan autentikasi menggunakan objek adminAuthenticator
    boolean isAuthenticated = adminAuthenticator.authenticate(username, passwordChars);

    if (isAuthenticated) {
        // Jika autentikasi berhasil, buka antarmuka admin_main
        admin_main admin = new admin_main(username);
        admin.setVisible(true);
        dispose();
    } else {
        JOptionPane.showMessageDialog(rootPane, "Gagal login. Periksa kembali nama pengguna dan kata sandi.");
    }
    }//GEN-LAST:event_jLabel2MouseClicked

